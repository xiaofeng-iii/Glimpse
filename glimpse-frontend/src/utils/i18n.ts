export type MessageKey = keyof typeof messages['zh-CN']

export const languagePreference = ref<LanguagePreference>('zh-CN')

export const normalizeLanguagePreference = (value: unknown): LanguagePreference => {
  return typeof value === 'string' && languages.has(value as LanguagePreference)
    ? (value as LanguagePreference)
    : 'zh-CN'
}

export const setLanguagePreference = (value: unknown) => {
  const normalized = normalizeLanguagePreference(value)
  languagePreference.value = normalized
  document.documentElement.lang = normalized
  window.localStorage.setItem(storageKey, normalized)
  return normalized
}

export const getStoredLanguagePreference = () => {
  return normalizeLanguagePreference(window.localStorage.getItem(storageKey))
}

export const t = (key: MessageKey, replacements: Record<string, string | number> = {}) => {
  let message: string = messages[languagePreference.value][key] ?? messages['zh-CN'][key] ?? key
  for (const [name, value] of Object.entries(replacements)) {
    message = message.split(`{${name}}`).join(String(value))
  }
  return message
}
